def _interpolate_from_table(
    table: dict[int | float, float],
    key: int | float,
    max_int_key: int = 200,
) -> Optional[float]:
    """Linear interpolation between table entries.

    Parameters
    ----------
    table : dict
        Mapping of sample-size (or df) to value.
    key : int | float
        Requested sample-size / df.
    max_int_key : int
        Values above this threshold fall back to the infinity entry.

    Returns
    -------
    float | None
        Interpolated value or ``None`` when the key cannot be resolved.
    """
    if key in table:
        return table[key]

    if key > max_int_key:
        return table.get(float("inf"))

    int_keys = sorted(k for k in table if isinstance(k, (int, float)) and not math.isinf(k))
    for i in range(len(int_keys) - 1):
        if int_keys[i] < key < int_keys[i + 1]:
            v1, v2 = table[int_keys[i]], table[int_keys[i + 1]]
            weight = (key - int_keys[i]) / (int_keys[i + 1] - int_keys[i])
            return v1 + weight * (v2 - v1)

    return None


def get_k_factor(
    n: int,
    confidence: float,
    coverage: float,
    bilateral: bool = True,
) -> Optional[float]:
    """Get k-factor for tolerance interval calculation (ISO 16269-6).

    Parameters
    ----------
    n : int
        Sample size.
    confidence : float
        Confidence level (0.90, 0.95, or 0.99).
    coverage : float
        Coverage proportion (0.90, 0.95, or 0.99).
    bilateral : bool
        ``True`` for two-sided, ``False`` for one-sided.

    Returns
    -------
    float | None
        k-factor, or ``None`` when the combination is not in the tables.
    """
    conf_str = str(int(confidence * 100))
    cov_str = str(int(coverage * 100))

    try:
        table = (BILATERAL_K_FACTORS if bilateral else UNILATERAL_K_FACTORS)[conf_str][cov_str]
    except KeyError:
        return None

    return _interpolate_from_table(table, n)


def get_t_value(
    df: int,
    confidence: float,
    bilateral: bool = True,
) -> Optional[float]:
    """Get t-distribution critical value (ISO 2602).

    Parameters
    ----------
    df : int
        Degrees of freedom (*n* - 1 for a single sample).
    confidence : float
        Confidence level (0.90, 0.95, or 0.99).
    bilateral : bool
        ``True`` for two-sided, ``False`` for one-sided.

    Returns
    -------
    float | None
        t-value, or ``None`` when the combination is not in the tables.
    """
    conf_str = str(int(confidence * 100))

    try:
        table = (T_VALUES_BILATERAL if bilateral else T_VALUES_UNILATERAL)[conf_str]
    except KeyError:
        return None

    return _interpolate_from_table(table, df, max_int_key=120)
